package day14_Math;

public class Demo1 {
	
	public static void main(String[] args){
		System.out.println(Math.PI);
		System.out.println("abs:"+Math.abs(-10));
		//ceil  天花板 12  13
		System.out.println("ceil:"+Math.ceil(12.3));			
		System.out.println("ceil:"+Math.ceil(12.99));
		System.out.println("floor:"+Math.floor(12.3));
		System.out.println("floor:"+Math.floor(12.56));
		System.out.println("max:"+Math.max(12,12.3));
		System.out.println("pow:"+Math.pow(12,2));
		System.out.println("random:"+Math.random());
		System.out.println("round:"+Math.round(12.3));
		System.out.println("round:"+Math.round(12.56));
		System.out.println("sqrt:"+Math.sqrt(4));
	}
}
