package nyist.net.po;

import java.util.Date;

public class Loss {
	
	private Integer LossId;
	private String LossInf;
	private Date LossTime;
	private String LossExa;
	private String LossStu;
	private String LossTel;
	
	
	
	public Integer getLossId() {
		return LossId;
	}
	public void setLossId(Integer lossId) {
		LossId = lossId;
	}
	public String getLossInf() {
		return LossInf;
	}
	public void setLossInf(String lossInf) {
		LossInf = lossInf;
	}
	public Date getLossTime() {
		return LossTime;
	}
	public void setLossTime(Date lossTime) {
		LossTime = lossTime;
	}
	public String getLossExa() {
		return LossExa;
	}
	public void setLossExa(String lossExa) {
		LossExa = lossExa;
	}
	public String getLossStu() {
		return LossStu;
	}
	public void setLossStu(String lossStu) {
		LossStu = lossStu;
	}
	public String getLossTel() {
		return LossTel;
	}
	public void setLossTel(String lossTel) {
		LossTel = lossTel;
	}
	

}
